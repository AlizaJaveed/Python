# Conditional Tests
a = 1
if a == 1:
    print(bool(a))
else:
    print(bool(a))


# !=
if a != 29:
    print(bool(a))
else:
    print(a)


# >
if a > 29:
    print(bool(a))
else:
    print(a)


# >=
if a >= 29:
    print(bool(a))
else:
    print(a)


# <
if a < 29:
    print(bool(a))
else:
    print(a)


# <=
if a <= 29:
    print(bool(a))
else:
    print(a)